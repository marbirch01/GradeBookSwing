    public ClassesForm(String title, ClassContainer container) {
        super(title);
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.setContentPane(classesPanel);
        this.pack();
        createClassesTable(container);
        addClass.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String msg = "";
                if (classesTable.getSelectionModel().isSelectionEmpty()) {
                    if (addClassName.getText().isEmpty() || classMaxNumberOfStudents.getText().isEmpty())
                        msg = "Wypełnij wszystkie pola, aby stworzyć klasę";
                    else {
                        try {
                            Class newClass = new Class(addClassName.getText(), new ArrayList<>(), Integer.parseInt(classMaxNumberOfStudents.getText()));
                            container.addClass(newClass.groupName, newClass);
                        } catch (NumberFormatException error) {
                            msg = "Wpisz liczbę całkowitą w polu Maksymalna ilość studentów";
                        }
                    }
                } else {
                    if (addClassName.getText().isEmpty())
                        addClassName.setText(classesTable.getValueAt(classesTable.getSelectedRow(), 0).toString());
                    if (classMaxNumberOfStudents.getText().isEmpty())
                        classMaxNumberOfStudents.setText(classesTable.getValueAt(classesTable.getSelectedRow(), 2).toString());
                    try {
                        msg = container.changeClass(classesTable.getValueAt(classesTable.getSelectedRow(), 0).toString(), addClassName.getText(), Integer.parseInt(classMaxNumberOfStudents.getText()));
                    } catch (NumberFormatException error) {
                        msg = "YWpisz liczbę całkowitą w polu Maksymalna ilość studentów";
                    }
                }
                if (msg != "") JOptionPane.showMessageDialog(null, msg);
                createClassesTable(container);
                addClassName.setText("");
                classMaxNumberOfStudents.setText("");
                addClassLabel.setText("Dodaj klase");
                removeClassPanel.setVisible(true);
                removeClassLabel.setText("Usuń klase");
                addClass.setText("Dodaj");
            }
        });
        removeClass.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String msg = "";
                if (classesTable.getSelectionModel().isSelectionEmpty()) {
                    if (removeClassName.getText().isEmpty())
                        msg = "Wpisz nazwę klasy, którą chcesz usunąć lub wybierz klasę z tabeli";
                    else container.removeClass(removeClassName.getText());
                } else {
                    container.removeClass(classesTable.getValueAt(classesTable.getSelectedRow(), 0).toString());
                }
                if (msg != "") JOptionPane.showMessageDialog(null, msg);
                createClassesTable(container);
                removeClassName.setText("");
                addClassLabel.setText("Dodaj klase");
                removeClassPanel.setVisible(true);
                removeClassLabel.setText("Usuń klase");
                addClass.setText("Dodaj");
            }
        });
        findStudent.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (!classFindStudent.getText().isEmpty()) {
                    List<Student> searchedStudent = container.searchStudent(classFindStudent.getText());
                    String msg = "";
                    for (Student currentStudent : searchedStudent) {
                        msg += currentStudent.toString();
                    }
                    if (msg.isEmpty()) msg = "Uczeń o podanym nazwisku nie występuję w żadnej klasie";
                    JOptionPane.showMessageDialog(null, msg);
                } else JOptionPane.showMessageDialog(null, "W odpowiednim polu wpisz nazwisko które chcesz wyszukać");
            }
        });
        classesTable.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {
                JTable table = (JTable) mouseEvent.getSource();
                Point point = mouseEvent.getPoint();
                int row = table.rowAtPoint(point);
                if (mouseEvent.getClickCount() == 2 && table.getSelectedRow() != -1) {
                    String clickedClass = classesTable.getValueAt(classesTable.getSelectedRow(), 0).toString();
                    container.classMap.forEach((key, value) ->
                    {
                        if (key.equals(clickedClass)) {
                            JFrame gbf = new StudentsForm(key, value);
                            gbf.setVisible(true);
                            gbf.setSize(1000, 900);
                        }
                    });
                }
                if (mouseEvent.getClickCount() == 1 && table.getSelectedRow() != -1) {
                    addClassLabel.setText("Edytuj klase");
                    removeClassLabel.setText("Usuń wybraną klase");
                    removeClassPanel.setVisible(false);
                    addClass.setText("Edytuj");
                }
            }
        });
        classesPanel.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {
                Point point = mouseEvent.getPoint();
                if (mouseEvent.getClickCount() == 1) {
                    classesTable.clearSelection();
                    addClassLabel.setText("Dodaj klase");
                    removeClassPanel.setVisible(true);
                    removeClassLabel.setText("Usuń klase");
                    addClass.setText("Dodaj");
                }
            }
        });
        if (this.hasFocus()) {
            createClassesTable(container);
        }
    }
    private void createClassesTable(ClassContainer container) {
        ClassContainerTableModel table = new ClassContainerTableModel(container.classMap);
        classesTable.setModel(table);
        if (container.classMap.isEmpty()) {
            removePanel.setVisible(false);
            findPanel.setVisible(false);
        } else {
            removePanel.setVisible(true);
            findPanel.setVisible(true);
        }
    }

